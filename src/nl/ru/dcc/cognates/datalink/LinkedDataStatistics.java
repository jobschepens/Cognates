package nl.ru.dcc.cognates.datalink;

public class LinkedDataStatistics {
	private int doubleMatches;
	private int srcMatches;
	private int desMatches;
	private int noMatches;
	private int noCognates;

	/**
	 * @param doubleMatches
	 * @param srcMatches
	 * @param desMatches
	 * @param noMatches
	 */
	public LinkedDataStatistics(int doubleMatches, int srcMatches,
			int desMatches, int noMatches) {
		this.doubleMatches = doubleMatches;
		this.srcMatches = srcMatches;
		this.desMatches = desMatches;
		this.noMatches = noMatches;
	}

	public int getDoubleMatches() {
		return doubleMatches;
	}

	public void setDoubleMatches(int doubleMatches) {
		this.doubleMatches = doubleMatches;
	}

	public int getSrcMatches() {
		return srcMatches;
	}

	public void setSrcMatches(int srcMatches) {
		this.srcMatches = srcMatches;
	}

	public int getDesMatches() {
		return desMatches;
	}

	public void setDesMatches(int desMatches) {
		this.desMatches = desMatches;
	}

	public int getNoMatches() {
		return noMatches;
	}

	public void setNoMatches(int noMatches) {
		this.noMatches = noMatches;
	}

	/**
	 * @return
	 */
	public int getCognates() {
		return noCognates;
	}

	/**
	 * @param i
	 */
	public void setCognates(int noCognates) {
		this.noCognates = noCognates;
	}
}